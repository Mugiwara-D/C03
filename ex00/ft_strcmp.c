int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int r;

	i = 0;
	r = 0;
	while (s1[i])
		i++;
	while (s2[r])
		r++;
	if ((i - r) < 0)
		return (-1);
	else if ((i - r) > 0)
		return (1);
	else
		return (0);
}
